example = 'Vladimir'
print(example[0])       # вывод на экран первого символа строки.
print(example[-1])      # вывод на экран последнего символа строки.
print(example[4:])      # вывод на экран второй половины строки.
print(example[::-1])    # вывод на экран строки наоборот.
print(example[1::2])     # вывод на экран каждого второго символа строки.




